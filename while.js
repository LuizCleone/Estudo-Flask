//estrutura de repetição
// Usando while para repetir uma ação enquanto uma condição for verdadeira
let contador = 0;
while (contador < 5) {
    console.log(`Contador: ${contador}`);
    contador = contador + 1; // Incrementa o contador
    // contador += 1; // Outra forma de incrementar o contador
    // contador++; // Incrementa o contador
      
}